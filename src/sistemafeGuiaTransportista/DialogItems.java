    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        if (evt.getClickCount() == 1) {
            ItemCANTIDAD = jTextField3.getText();
            /*System.out.println(ItemID);
            System.out.println(ItemDESCRIP);
            System.out.println(ItemUNIDAD);
            System.out.println(ItemCANTIDAD);*/
            if (ItemID.equals("") || ItemDESCRIP.equals("") || ItemUNIDAD.equals("") || ItemCANTIDAD.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan datos para el articulo", "ยก Mensaje !", JOptionPane.INFORMATION_MESSAGE);
            } else {
                if (esNumero(ItemCANTIDAD) == false) {
                    jTextField3.setText("");
                    JOptionPane.showMessageDialog(null, "Cantida no debe de contener letras!", "ยก Error !", JOptionPane.INFORMATION_MESSAGE);
                } else {
                try {
                    FrameGTRregister.GTRItemsId.add(ItemID);
                    FrameGTRregister.GTRItemsDesc.add(ItemDESCRIP);
                    FrameGTRregister.GTRItemsUnidad.add(ItemUNIDAD);
                    FrameGTRregister.GTRItemsCantidad.add(ItemCANTIDAD);
                    FrameGTRregister GTRr = new FrameGTRregister();
                    GTRr.LLenarCampos();
                    GTRr.setVisible(true);
                    this.setVisible(false);
                } catch (Exception ex) {
                    Logger.getLogger(DialogItems.class.getName()).log(Level.SEVERE, null, ex);
                }
            }}
        }

    }//GEN-LAST:event_jButton2MouseClicked
private static boolean esNumero(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }
